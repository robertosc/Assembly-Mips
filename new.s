main:
	jal data_req

data_req:
	addi $v0, $0, 5 	# 001000 00000 00010 0000000000000101
						# 0010 0000 0000 0010 0000 0000 0000 0101 = 20020005
	syscall				#
	
	add $a0, $0, $v0	# 000000 00000 00010 00100 00000 100000
						# 0000 0000 0000 0010 0010 0000 0010 0000 = 00022020
	jal sum_recursive	# 000011 sum_recusive

	add $a0, $0, $v0	# 000000 00000 00010 00100 00000 100000
						# 0000 0000 0000 0010 0010 0000 0010 0000 = 00022020

	addi $v0, $0, 1		# 001000 00000 00010 0000000000000001
						# 0010 0000 0000 0010 0000 0000 0000 0001 = 20020001 ###Revisar
	syscall

	addi $v0, $0, 10	# 001000 00000 00010 0000000000001010
						# 0010 0000 0000 0010 0000 0000 0000 1010 = 2002000A
	syscall

sum_recursive:
	addi $sp, $sp, -8	# 001000 11101 11101 1111111111111000
						# 0010 0011 1011 1101 1111 1111 1111 1000 = 23BDFFF8

	sw $ra, 0($sp)		# 101011 11101 11111 0000000000000000
						# 1010 1111 1011 1111 0000 0000 0000 0000 = AFBF00000

	sw $a0, 4($sp)		# 101011 11101 00100 0000000000000100
						# 1010 1111 1010 0100 0000 0000 0000 0100 = AFA40004

	bne $a0, $0, min	

	addi $v0, $0, 0		# 001000 00000 00010 0000000000000000
						# 0010 0000 0000 0010 0000 0000 0000 0000 = 20020000

	addi $sp, $sp, 8	# 001000 11101 11101 0000000000001000
						# 0010 0011 1011 1101 0000 0000 0000 1000 = 23BD0008
	
	jr $ra				# 000000 11111 00000 00000 00000 001000
						# 0000 0011 1110 0000 0000 0000 0000 1000 = 03E00008

min:
	addi $a0, $a0, -1	# 001000 00100 00100 1111111111111111
						# 0010 0000 1000 0100 1111 1111 1111 1111 = 2082FFFF
	jal sum_recursive

	lw $a0, 4($sp)		# 100011 11101 00100 0000000000000100
						# 1000 1111 1010 0100 0000 0000 0000 0100 = 8FA40004
	lw $ra, 0($sp)		# 100011 11101 11111 0000000000000000
						# 1000 1111 1011 1111 0000 0000 0000 0000 = 8FBF0000
	addi $sp, $sp, 8	# 001000 11101 11101 0000000000001000
						# 0010 0011 1011 1101 0000 0000 0000 1000 = 23BD0008

	add $v0, $v0, $a0	# 000000 00010 00100 00010 00000 100000
						# 0000 0000 0100 0100 0001 0000 0010 0000 = 00441020

	
