main:
	addi $a0, $0, 5		# 001000 00000 00100 0000000000000101
						# 0010 0000 0000 0100 0000 0000 0000 0101 = 20040005

	addi $t1, $0, 5		# 001000 00000 01001 0000000000000101
						# 0010 0000 0000 1001 0000 0000 0000 0101 = 20090005

	addi $t0, $0, 1		# 001000 00000 01000 0000000000000001
						# 0010 0000 0000 1000 0000 0000 0000 0001 = 20080001

	#la $ra, end
	beq $t0, $t0, sum_recursive # 000100 01000 01000 sum_recursive
	nop

sum_recursive:

	sw $ra, 0($sp)		# 101011 11101 11111 0000000000000000
						# 1010 1111 1011 1111 0000 0000 0000 0000 = AFBF0000

	sw $a0, 4($sp)		# 101011 11101 00100 0000000000000100
						# 1010 1111 1010 0100 0000 0000 0000 0100 = AFA40004
	
	addi $sp, $sp, 8	# 001000 11101 11101 0000000000001000
						# 0010 0011 1011 1101 0000 0000 0000 1000 = 23BD0008
	
	slt $t2, $a0, $t0	# 101010 00100 01000 01010 00000 000000
						# 1010 1000 1000 1000 0101 0000 0000 0000 = A8885000
	
	beq $t2, $0, min		# 000100 01010 00000 min	

	addi $v0, $0, 0		# 001000 00000 00010 0000000000000000
						# 0010 0000 0000 0010 0000 0000 0000 0000 = 20020000

	#addi $sp, $sp, 8	# 001000 11101 11101 0000000000001000
						# 0010 0011 1011 1101 0000 0000 0000 1000 = 23BD0008
	
	beq $t0, $t0, post	# 000100 01000 01000 post


min:
	addi $a0, $a0, -1	# 001000 00100 00100 1111111111111111
						# 0010 0000 1000 0100 1111 1111 1111 1111 = 2082FFFF***2-4

	#la $ra, post
	beq $t0, $t0, sum_recursive # 000100 01000 01000 sum_recursive
	nop

post:
	lw $a0, 4($sp)		# 100011 11101 00100 0000000000000100
						# 1000 1111 1010 0100 0000 0000 0000 0100 = 8FA40004
						
	lw $ra, 0($sp)		# 100011 11101 11111 0000000000000000	
						# 1000 1111 1011 1111 0000 0000 0000 0000 = 8FBF0000
	
	addi $sp, $sp, -8	# 001000 11101 11101 111111111111000
						# 0010 0011 1011 1101 1111 1111 1111 1000 = 23BDFFF8

	add $v0, $v0, $a0	# 000000 00010 00100 00010 00000 100000
						# 0000 0000 0100 0100 0001 0000 0010 0000 = 00441020
	
	slt $t2, $a0, $t1	# 101010 00100 01001 01010 00000 000000
						# 1010 1000 1000 1001 0101 0000 0000 0000 = A8895000

	beq $t2, $0, end		# 000100 01010 00000 end
#	bge $a0, $t1, end

	beq $t0, $t0, post	# 000100 01000 01000 post
